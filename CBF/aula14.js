// na aula 14 vamos falar do comado switch case

let colocacao = 5
switch(colocacao){
    case 1:
        console.log("Primeiro lugar!")
        break
    case 2:
        console.log("Segundo lugar!")
        break
    case 3:
        console.log("Terceiro lugar!")
        break
    case 4:
        console.log("Quarto lugar!")
        break
    default:
        console.log("Nao subiu no podio")
}